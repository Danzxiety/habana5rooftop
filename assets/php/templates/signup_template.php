  $(document).ready(function() {
  // Verifica cuál div está activo y cambia el ancho de la barra de progreso
  function updateProgressBar() {
    if ($('#basicFormAccount').is(':visible')) {
      $('#progress').css('width', '30%');
    } else if ($('#basicFormProfile').is(':visible')) {
      $('#progress').css('width', '60%');
    } else if ($('#basicFormAddress').is(':visible')) {
      $('#progress').css('width', '100%');
    }
  }

  // Llama a la función cuando el documento está listo
  updateProgressBar();

  // También puedes llamar a la función cuando se hace clic en algún botón o enlace que cambia el div activo
  $('button, a').click(function() {
    // Puedes agregar un pequeño retraso para asegurarte de que el div activo ha cambiado antes de llamar a la función
