// Esta función verifica si todos los campos de entrada están llenos.
function checkInputs() {
  var inputs = document.getElementsByClassName('induo');
  for (var i = 0; i < inputs.length; i++) {
    if (inputs[i].value === '') {
      return false;
    }
  }
  return true;
}

// Esta función habilita o deshabilita el botón dependiendo de si todos los campos de entrada están llenos.
function updateButton() {
  var button = document.getElementById('checkduo');
  if (checkInputs()) {
    button.disabled = false;
  } else {
    button.disabled = true;
  }
}

// Agrega un controlador de eventos a cada campo de entrada para actualizar el estado del botón cada vez que el usuario escribe algo.
var inputs = document.getElementsByClassName('induo');
for (var i = 0; i < inputs.length; i++) {
  inputs[i].addEventListener('input', updateButton);
}

// Deshabilita el botón inicialmente.
document.getElementById('checkduo').disabled = true;

